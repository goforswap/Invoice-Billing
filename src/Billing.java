    public void setInvoiceNo() throws IOException
    {
        File file = new File("invoiceNo.txt");
        BufferedReader br = new BufferedReader(new FileReader(file)); 
        String st = br.readLine();
        tfInvoiceNo.setText(st);
        tfInvoiceNo.setEditable(false);
        br.close();
        int no = Integer.parseInt(st);
        no = no+1;
        BufferedWriter bw = new BufferedWriter(new FileWriter(file));
        bw.write(Integer.toString(no));    
        bw.close();
    }
    
