    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            if(tfName.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(Billing.this, "Please Enter the Name of Product you want to Purchase.");
                return;
            }
            tfQty.setText("");
            tfAmount.setText("");
            Scanner sc = new Scanner(new File("PRODUCTS.csv"));
            sc.useDelimiter(",|\\n");
            while(sc.hasNext())
                if(sc.next().trim().equals(tfName.getText()))
                {
                    String PID = sc.next();
                    tfPID.setText(PID);
                    String Unit = sc.next();
                    tfUnit.setText(Unit);
                    String Price = sc.next();
                    tfPrice.setText(Price);
                    String Discount = sc.next();
                    tfDiscount.setText(Discount+"%");
                }
            sc.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Billing.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

