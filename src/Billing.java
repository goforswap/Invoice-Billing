    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if( tblProd.getRowCount() == 0 )
        {
            JOptionPane.showMessageDialog(Billing.this, "The CART is Already EMPTY.");
            return;
        }
        
        if ( tblProd.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(Billing.this, "Please Select The Product From the Table To REMOVE it.");
            return;
        }
        int p = JOptionPane.showConfirmDialog(null,"Are You Sure you want to Delete The Product?", "Delete A Product", JOptionPane.YES_NO_OPTION);
        if(p==0)
        {
            if(tblProd.getRowCount()==1)
            {
                no_items=0;
                cgst=0;
                sgst=0;
                total=0;
                tfNo.setText("");
                tfCGST.setText("");
                tfSGST.setText("");
                tfTotal.setText("");
            }
            else
            {
                no_items = no_items-1;
                String no = Integer.toString(no_items);
                tfNo.setText(no);
                int row = tblProd.getSelectedRow();
                String str = (tblProd.getModel().getValueAt(row,6).toString());
                int TOTAL = Integer.parseInt(str);
                total = total - TOTAL - cgst - sgst;
                cgst = ((total*6)/100);
                sgst = ((total*6)/100);
                String CGST = Integer.toString(cgst);
                tfCGST.setText(CGST);
                String SGST = Integer.toString(sgst);
                tfSGST.setText(SGST);
                total = total + cgst + sgst;
                String Total = Integer.toString(total);
                tfTotal.setText(Total);
            }
            model.removeRow(tblProd.getSelectedRow());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

