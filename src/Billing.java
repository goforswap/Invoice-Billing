    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tfPrice.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(Billing.this, "Please Click on \"SEARCH ITEM\" before Calculating the Price of a Product.");
            return;
        }
        if(tfQty.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(Billing.this, "Please Enter the Quantity of the Product you Want to Purchase");
            return;
        }
        String Price = tfPrice.getText();
        String Quantity = tfQty.getText();
        String Discount = tfDiscount.getText();
        int Disc_len = Discount.length();
        Discount = Discount.substring(0, Disc_len-2);
        int price = Integer.parseInt(Price);
        int quantity = Integer.parseInt(Quantity);
        int discount = Integer.parseInt(Discount);
        price = price - ((price*discount)/100);
        int amount = price * quantity;
        String Amount = Integer.toString(amount);
        tfAmount.setText(Amount);
    }//GEN-LAST:event_jButton2ActionPerformed

